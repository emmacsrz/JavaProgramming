package Day16_WhileLoopContd_DoWhileLoop_NestedLoop;

public class DoWhileLoop {
    public static void main(String[] args) {

        boolean condition = false;


        for (; condition; ) {
            System.out.println("hello Cydeo");
        }

        System.out.println("-------------------");

        do {
            System.out.println("Hello cydeo");
        } while (condition);





    }
}
