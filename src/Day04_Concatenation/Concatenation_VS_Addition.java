package Day04_Concatenation;

public class Concatenation_VS_Addition {
    public static void main(String[] args) {

        System.out.println("32" + 1 ); // prints as "321".  the +1 is concated to the "32" (the "32 is a string of text)

        System.out.println(7+ "1"); //prints as 71.  Same concept, concatenation is on the other side though.

        System.out.println("~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~");

        System.out.println(12 + 2.5); //will result in addition printing.  if you use  astring, it will not add them, it will just print numbers next to eachother. That's what a concatenation is.







    }
}
