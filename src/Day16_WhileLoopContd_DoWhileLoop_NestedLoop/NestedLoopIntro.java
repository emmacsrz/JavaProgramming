package Day16_WhileLoopContd_DoWhileLoop_NestedLoop;

public class NestedLoopIntro {
    public static void main(String[] args) {


        System.out.println("------------------------");


        for (int i = 0; i<5; i++){  //this is the outer loop will be executed 5 times
            for (int j = 0; j < 5; j++) { //this is the inner loop will be executed 5 times EVERY TIME THE OUTER LOOP IS EXECUTED.
                System.out.println("wooden spoon");
            }
        }






    }
}
